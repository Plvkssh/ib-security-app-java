export default function TrainingPage() {
  const [open, setOpen] = useState({})

  const toggle = (id) => {
    setOpen(prev => ({ ...prev, [id]: !prev[id] }))
  }

  return (
    <div>
      <h1 className="text-2xl font-bold text-slate-900 dark:text-white mb-2">Обучающие материалы</h1>
      <p className="text-sm text-slate-500 dark:text-slate-400 mb-6">Справочник по кибербезопасности для сотрудников. Адаптирован под российское законодательство.</p>

      <div className="space-y-3">
        {sections.map(sec => (
          <div key={sec.id} className="bg-white dark:bg-slate-900 rounded-xl border border-slate-200 dark:border-slate-800 overflow-hidden" data-testid={`training-${sec.id}`}>
            <button
              onClick={() => toggle(sec.id)}
              data-testid={`toggle-${sec.id}`}
              className="w-full flex items-center gap-3 p-4 text-left hover:bg-slate-50 dark:hover:bg-slate-800/50 transition-colors"
            >
              <div className="w-9 h-9 rounded-lg bg-cyan-50 dark:bg-cyan-950 flex items-center justify-center flex-shrink-0">
                <sec.icon size={18} className="text-cyan-600 dark:text-cyan-400" />
              </div>
              <span className="text-sm font-medium text-slate-900 dark:text-white flex-1">{sec.title}</span>
              {open[sec.id] ? <ChevronDown size={16} className="text-slate-400" /> : <ChevronRight size={16} className="text-slate-400" />}
            </button>
            {open[sec.id] && (
              <div className="px-4 pb-4 pt-0">
                <div className="pl-12 text-sm text-slate-600 dark:text-slate-400 leading-relaxed whitespace-pre-line">
                  {sec.content}
                </div>
              </div>
            )}
          </div>
        ))}
      </div>
    </div>
  )
}
